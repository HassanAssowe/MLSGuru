        self.points = points
        self.wins = wins
        self.losses = losses
        self.ties = ties

        self.goals = goals
        self.goals_against = goals_against
        self.goals_difference = goals_difference

        self.penalty_kick_taken = penalty_kick_taken
        self.penalty_conceded = penalty_conceded

        self.fouls_committed = fouls_committed
        self.fouls_suffered = fouls_suffered

        self.total_offside = total_offside

        self.yellow_card = yellow_card
        self.red_card = red_card



